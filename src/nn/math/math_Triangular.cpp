void SinCosIdx(f32* pSin, f32* pCos, u16 idx){
    u16 i = idx >> 8;
    u16 d = idx & 0xff;
    f32 fd = U16ToF32(d) * (1.f / 256.f);
    f32 sVal = internal::gSinCosTbl[i].sin_val + fd * internal::gSinCosTbl[i].sin_delta;
    f32 cVal = internal::gSinCosTbl[i].cos_val + fd * internal::gSinCosTbl[i].cos_delta;

    *pSin = (idx >> 31) ? -sVal : sVal;
    *pCos = cVal;
}

f32 SinFIdx(f32 fidx){
    f32 abs_fidx;
    f32 val;
    u16 idx;
    f32 r;

    abs_fidx = FAbs(fidx);
    while(abs_fidx >= 65536.0f){
        abs_fidx -= 65536.0f;
    }

    idx = F32ToU16(abs_fidx);
    r = abs_fidx - U16ToF32(idx);
    idx &= 0xff;

    val = internal::gSinCosTbl[idx].sin_val + r * internal::gSinCosTbl[idx].sin_delta;

    return (fidx < 0.0f) ? -val: val;
}

f32 CosFIdx(f32 fidx){
    u16 idx;
    f32 r;

    fidx = FAbs(fidx);
    while( fidx >= 65536.0f )
    {
        fidx -= 65536.0f;
    }

    idx = F32ToU16(fidx);
    r = fidx - U16ToF32(idx);
    idx &= 0xff;

    return internal::gSinCosTbl[idx].cos_val + r * internal::gSinCosTbl[idx].cos_delta;
}

f32 AtanFIdx(f32 x){
    if(x >= 0.f){
        if(x > 1.f){
            return 64.f - AtanFIdx_(1.f/x);
        }
        else{
            return AtanFIdx_(x);
        }
    }
    else{
        if(x < -1.f){
            return -64.f + AtanFIdx_(-1.f/x);
        }
        else{
            return - AtanFIdx_(-x);
        }
    }

}

